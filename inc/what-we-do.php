          <h2>ALICE</h2>
          <p>ALICE stands for Asset Limited, Income Constrained, Employed. ALICE refers to those who earn above the federal poverty level, but not enough to afford housing, child care, food, transportation, and health care in our community. ALICE has to choose between food or electricity, diapers or medicine, and rent or childcare.</p>
          <p>The ALICE report is an in-depth and more accurate measure of financial struggle in our corner of the country. This groundbreaking report from United Way provides state, county, and city specific data.</p>
          <p>The most recent ALICE report shows that 39% of our local population lives at or below the ALICE threshold. ALICE is the person you sat next to on the bus, that friendly barista who poured your coffee this morning, your neighbor, your friend. ALICE is all around you, and she needs your help.</p>
