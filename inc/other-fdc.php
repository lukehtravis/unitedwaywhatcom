      <h1>Fund Distribution Committee</h1>
      <p>The Fund Distribution Committee (FDC) is group of community volunteers that review all programs funded by the United Way of Whatcom County and gives recommendations for grants to the fdc of Directors. Volunteers are divided into three groups and meet on either a Monday, Tuesday, or Wednesday afternoon for four weeks. Each panel is responsible for reviewing approximately 6-10 year-end reports and 3-6 new grant proposals.</p>
