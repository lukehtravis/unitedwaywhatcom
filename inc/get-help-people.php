        <h2>FamilyWize</h2>
        <p>Healthcare is expensive. Over 80 million Americans are uninsured or underinsured. They can’t afford meds. 14% of people don't get their prescriptions filled.</p>
        <p>As a Whatcom County resident, we don’t just want you to survive. We want you to thrive. Decreasing your prescription drug payments can help with that. We’ve teamed up with the FamilyWize® Community Service Partnership, Inc. to make it possible to get your meds and pay for the other stuff you need, like lunch and textbooks.</p>
        <p>Everyone is eligible. FamilyWize prescription drug discount cards have zero qualifying limitations. No usage, age, or income limits.</p>
        <p>It’s easy to use. Simply give the card to your participating pharmacist with your prescription.  An instant discount (varies by medication) is applied to the prescription medicine.</p>
        <p>You’ll get the lowest price possible. The card provides pharmacy discounts (up to 75%) on all FDA approved prescription medicine not covered by insurance, Medicaid, Medicare, or other benefit plans.  You pay either the discounted price or the pharmacy's retail price, whichever is lower.</p>
