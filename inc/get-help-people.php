        <h2>Project Homeless Connect</h2>
        <p>Project Homeless Connect (PHC) is an international initiative sponsored by the U.S. Interagency Council on Homelessness (USICH). It has been implemented in well over 700 communities across the United States, Canada, and Australia. Our local PHC was first adopted in 2007 during Phase 3 of the Whatcom County Plan to End Homelessness. United Way of Whatcom County continues as the fiscal agent.</p>
        <p>This event is designed to reduce barriers to services. Services available for free during PHC include:</p>
        <ul>
          <li>dental exams and services</li>
          <li>general medical exams</li>
          <li>haircuts</li>
          <li>vision and hearing screenings</li>
          <li>housing intakes</li>
          <li>DOL photo ID</li>
          <li>foot care</li>
          <li>employment screenings</li>
          <li>health insurance assistance</li>
